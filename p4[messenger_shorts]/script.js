var story_div = document.querySelector(".stories")

arr.forEach(function (element, index) {
    clutter += `<div class="story">
                    <img id="${index}" src="${element.dp}" alt="">
                </div>`
});

story_div.innerHTML = clutter;

story_div.addEventListener("click", function (dots) {
    var clickedId = dots.target.getAttribute("id");
    var fullScreen = document.querySelector(".full-screen");

    fullScreen.style.display = "block";
    fullScreen.style.backgroundImage = `url(${arr[dots.target.id].story})`;

    console.log(arr[clickedId].story)

    fullScreen.offsetHeight; // This line is crucial for the transition to work
    fullScreen.style.opacity = "1";

    setTimeout(() => {
        document.querySelector(".full-screen").style.display = "none";
        fullScreen.style.opacity = "0";
    }, 1500);
});

